package javaBasics.operators.unaryOperator;

public class Demo1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        int c = 5;

        System.out.println(a++);    // 5
        System.out.println(b = b + 1);  // 6
        System.out.println(c += 1);     // 6

    }
}
